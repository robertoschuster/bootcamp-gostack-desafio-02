import express from 'express';
import routes from './routes';

// Cria conexão com o banco e inicializa os models com esta conexão
import './database';

class App {
  constructor() {
    this.server = express();
    this.middlewares();
    this.routes();
  }

  middlewares() {
    this.server.use(express.json());
  }

  routes() {
    this.server.use(routes);
  }
}

export default new App().server;
